void preCompute(int line, bool last) {
    string aux = nums[line], sub;
    for (int i = 0; i < possibleCombinations[aux].size(); i++) {
        sub = "";
        int combination = possibleCombinations[aux][i];

        int total = 0, lastNum = 0;
        int lastPos = 0;
        vector<int> temp;
        bool valid = true;
        for (int bit = 0; bit < aux.size(); bit++) {
            if (combination & (1 << bit)) {
                sub = aux.substr(lastPos, bit - lastPos + 1);
                if ((sub.size() >= 5 && !last) || sub.size() == 0 || (sub[0] == '0' && sub.size() > 1) ) {
                    valid = false;
                    break;
                }
                try {
                    lastNum = stoi(sub);
                    temp.push_back(lastNum);
                    total += lastNum;
                } catch (const invalid_argument& e) {
                    valid = false;
                    break;
                } catch (const out_of_range& e) {
                    valid = false;
                    break;
                }
                lastPos = bit + 1;
            }
        }

        if (!valid) continue;

        if (lastPos < aux.size()) {
            sub = aux.substr(lastPos);
            try {
                if (sub.size() <= 4 && !last && sub.size() > 0 && sub[0] != '0') {
                    lastNum = stoi(sub);
                    temp.push_back(lastNum);
                    total += lastNum;
                } else if (sub.size() <= 6 && last && sub.size() > 0 && sub[0] != '0') {
                    lastNum = stoi(sub);
                    temp.push_back(lastNum);
                    total += lastNum;
                } else if(sub.size() == 1){
                    lastNum = stoi(sub);
                    temp.push_back(lastNum);
                    total += lastNum;
                }
            } catch (const invalid_argument& e) {
                continue;
            } catch (const out_of_range& e) {
                continue;
            }
        }
        if (total - lastNum == lastNum) preComputation[line].push_back(temp);
        
    }
}


bool back(int line){
    if(line == nums.size()){
        vector<int> aux (report[0].size(), 0);
        for(int i = 0; i < report.size() - 1; i++){
            for(int j = 0; j < report[i].size(); j++){
                aux[j] += report[i][j];
            }
        }
        for(int i = 0; i < aux.size(); i++){
            if(aux[i] != report[report.size()-1][i]) return false;
            
        }
        return true;
    }

    for(int i = 0; i < preComputation[line].size(); i++){
        report.pb(preComputation[line][i]);
        if(back(line+1)) return true;
        report.pop_back();
    }

    return false;
}

void generatepossibleCombinations(string num) {
    int n = num.size();
    if(possibleCombinations.find(num) != possibleCombinations.end()) return;
    int obj = cut - 1; vector<int> comb;
    
    int total = (1 << n);
    
    for (int mask = 0; mask < total; ++mask) {
        if (__builtin_popcount(mask) == obj) {
            comb.push_back(mask);
        }
    }
    possibleCombinations[num] = comb;
}

